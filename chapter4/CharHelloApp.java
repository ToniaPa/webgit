package gr.aueb.cf.chapter4;

/**
 * print Hello using chars
 */
public class CharHelloApp {

    public static void main(String[] args) {
        char h = 'H';
        char e = 'e';
        char l = 'l';
        char o = 'o';
        char exclamationMark = '!';

        System.out.print(h);
        System.out.print(e);
        System.out.print(l);
        System.out.print(l);
        System.out.print(o);
        System.out.print(exclamationMark);
    }
}
